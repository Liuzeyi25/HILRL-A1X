#!/bin/bash
# 检查哪些进程正在使用 RealSense 相机

echo "============================================================"
echo "🔍 检查 RealSense 相机占用情况"
echo "============================================================"
echo ""

echo "1️⃣ 检查使用视频设备的进程:"
echo "-----------------------------------------------------------"
lsof /dev/video* 2>/dev/null | grep -v "^COMMAND" || echo "   未找到占用视频设备的进程"
echo ""

echo "2️⃣ 检查 Python 进程:"
echo "-----------------------------------------------------------"
ps aux | grep python | grep -v grep || echo "   未找到 Python 进程"
echo ""

echo "3️⃣ 检查 realsense 相关进程:"
echo "-----------------------------------------------------------"
ps aux | grep -i realsense | grep -v grep || echo "   未找到 realsense 进程"
echo ""

echo "4️⃣ 检查所有视频设备:"
echo "-----------------------------------------------------------"
ls -la /dev/video* 2>/dev/null || echo "   未找到视频设备"
echo ""

echo "============================================================"
echo "💡 如果发现占用的进程，可以使用以下命令关闭:"
echo "   kill <PID>          # 正常关闭进程"
echo "   kill -9 <PID>       # 强制关闭进程"
echo "============================================================"
